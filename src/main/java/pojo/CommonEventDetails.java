package pojo;

public class CommonEventDetails extends EventDetail {
	private String host;
	private String type;
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
